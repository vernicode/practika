            <div class="main-container__block">
                <p>Our full name is the International Criminal Police Organization and we are an inter-governmental organization. We have 195 member countries, and we help police in all of them to work together to make the world a safer place.</p>
                <p>To do this, we enable them to share and access data on crimes and criminals, and we offer a range of technical and operational support.</p>
                <h2>—  Who makes up INTERPOL?</h2>
                <p>The General Secretariat coordinates our day-to-day activities to fight a range of crimes. Run by the Secretary General, it is staffed by both police and civilians and comprises a headquarters in Lyon, a global complex for innovation in Singapore and several satellite offices in different regions.  </p>
                <p>In each country, an INTERPOL National Central Bureau (NCB) provides the central point of contact for the General Secretariat and other NCBs. An NCB is run by national police officials and usually sits in the government ministry responsible for policing. The General Assembly is our governing body and it brings all countries together once a year to take decisions.</p>
                <div class="main-container__block_image">
                    <img src="src/images/How-we-work-INTERPOL.jpg" alt="" class="main-container_image">
                </div>
            </div>
            <div class="main-container__block">
                <h2>—  Connecting police</h2>
                <p>We connect all our countries via a communications system called I-24/7. Countries use this secure network to contact each other, and the General Secretariat. It also allows them to access our databases and services in real-time, from both central and remote locations.</p>
                <p> We also coordinate networks of police and experts in different crime areas, who come together through working groups and at conferences to share experiences and ideas.</p>
                <div class="main-container__block_image">
                    <img src="src/images/Member-connected.jpg" alt="" class="main-container_image">
                </div>
            </div>
            <div class="main-container__block">
                <h2>—  What we do</h2>
                <p>The General Secretariat provides a range of expertise and services to our member countries. We manage 19 police databases with information on crimes and criminals (from names and fingerprints to stolen passports), accessible in real-time to countries. </p>
                <p>We offer investigative support such as forensics, analysis, and assistance in locating fugitives around the world. Training is an important part of what we do in many areas so that officials know how to work efficiently with our services.</p>
                <p>This expertise supports national efforts in combating crimes across three global areas we consider the most pressing today; terrorism, cybercrime and organized crime.</p>
                <p>Officials working in each specialized crime area run a variety of different activities alongside member countries. This can be investigative support, field operations, training and networking.</p>
                <p>Importantly, since crimes evolve, we keep an eye on the future through research and development in international crime and trends.</p>
                <div class="main-container__block_image">
                    <img src="src/images/Strategy.jpg" alt="" class="main-container_image">
                </div>
                
            </div>
            <div class="main-container__block">
                <h2>—  A global platform</h2>
                <p>Today’s crimes are increasingly international. It is crucial that there is coordination among all the different players in maintaining a global security architecture.
                <p>Since INTERPOL is a global organization, it can provide this platform for cooperation; we enable police to work directly with their counterparts, even between countries which do not have diplomatic relations.</p>
                <p>We also provide a voice for police on the world stage, engaging with governments at the highest level to encourage this cooperation and use of our services.</p>
                <p>All our actions are politically neutral and taken within the limits of existing laws in different countries.</p>
